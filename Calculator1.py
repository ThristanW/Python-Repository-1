a = float(input("Value 1= "))
b = float(input("Value 2= "))
Command= input("Choose Action: ")

if Command == "+":
    print(a + b)

if Command == "-":
    print(a - b)
    
if Command == "x":
    print(a * b)
    
if Command == "/":
    print(a / b)

if Command == "%":
    print(a % b)

if Command == "^":
    print(a ** b)
